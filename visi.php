<b>MISI SEKOLAH</b><br>
"Untuk mewujudkan <b>visi</b> SMP Negeri 6 Semarang, sekolah mengembangkan <b>misi</b> di bawah ini."<br><br>

&emsp;1.Menjadikan keimanan dan ketaqwaan sebagai dasar melakukan aktifitas pendidikan<br>
&emsp;2.Mendorong dan meningkatkan pengamalan agama melalui kegiatan keagamaan secara &emsp;&emsp;intensif.<br>
&emsp;3.Meningkatkan penguasaan dan keterampilan dalam bidang IPTEK, olah raga dan seni &emsp;&emsp;budaya<br>
&emsp;4.Menjadikan sekolah yang siap berkompetisi di tingkat kota , Provinsi, maupun <br>&emsp;&emsp;Nasional<br>
&emsp;5.Mewujudkan sekolah yang bersih, rapi, hijau, dan nyaman.<br>
&emsp;6.Meningkatkan kepedulian terhadap lingkungan.<br><br><br>

<b>TUJUAN SEKOLAH DALAM 1 TAHUN KE DEPAN</b><br>
&emsp;1.Meningkatkan kualitas beribadah melalui sholat dzuhur berjamaah bagi yang beragama &emsp;&emsp; Islam dan persekutuan doa bagi yang beragama non Islam, peringatan hari-hari besar &emsp;&emsp; agama serta lomba-lomba keagamaan.<br>
&emsp;2.Mengoptimalkan pendidikan karakter dan nasionalisme, lalu lintas dan adiwiyata <br>&emsp;&emsp;melalui berbagai aktivitas pembiasaan, kegiatan terprogram dan terintegrasi dalam &emsp;&emsp; mata pelajaran.<br>
&emsp;3.Meningkatkan konsistensi pembiasaan berbahasa Jawa dan bahasa Inggris melalui &emsp;&emsp;pembiasaan "Dinten Basa Jawi" pada Hari Kamis dan "English Day" pada Hari Sabtu.<br>
&emsp;4.Meningkatkan penguasaan dan keterampilan peserta didik dalam ilmu pengetahuan dan &emsp;&emsp;teknologi, olah raga dan seni budaya.<br>
&emsp;5.Meningkatkan kualitas pembelajaran aktif kreatif inofativ dan menyenangkan yang &emsp;&emsp;dapat meningkatakan rata-rata, UN, US serta kelulusan 100%<br>
&emsp;6.Meningkatkan perolehan kejuaraan lomba mata pelajaran, olah raga, seni dan karya &emsp;&emsp;ilmiah melalui pengembangan diri yang berkualitas.<br>
&emsp;7.Mengoptimalkan  kepedulian warga sekolah terhadap lingkungan dalam kegiatan <br>&emsp;&emsp;kebersihan, penghijauna, dan 3R ( Reduce, Reuse, recycle ) sehingga terwujud sekolah &emsp;&emsp;yang bersih rapi, hijau dan nyaman.<br>	
