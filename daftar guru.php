<h2>Sejarah Singkat</h2><br><br>
&emsp;&emsp;SMP Negeri 06 Semarang yang berada dijalur utama pusat kota yaitu di Jalan<br> &emsp;Pattimura 
No. 09 Semarang memiliki perjalanan sejarah yang panjang, sejak tahun 1940. <br>&emsp;Sejarah singkat berdirinya
SMP Negeri 06 Semarang, sejak tahun 1940 pada masa <br>&emsp;Penjajahan Hindia Belanda sebagai sekolah HIS.
Yaitu dalam perkembangan selanjutnya <br>&emsp;gedung HIS dipergunakan untuk SMEP Negeri Semarang atas dasar
SK  No. B.13/7/14 <br>&emsp;tgl, <br>&emsp;23-3-1954 dari Pemerintah Daerah kepada Menteri P dan K dengan hak sewa 
gedung. <br>&emsp;Kemudian perkembangan selanjutnya dengan SK Mendikbud RI No. 030/U/1979 tgl. 17-<br>&emsp;2- 1979 <br>&emsp;dialih 
fungsikan dari SMEP menjadi SMPT (Transisi) 06 Semarang. Adapun <br>&emsp;perkembangan berikutnya sejak tahun 1980 
menjadi SMP Negeri 06 Semarang yang <br>&emsp;berdasarkan SK Mendikbud No. 035 dan 036/O/97 tanggal 3-2-1997 dan 
berdasarkan <br>&emsp;Surat <br>&emsp;Sekretaris Jendral Depdikbud No. 41007/A.5.3/OT/97. <br>&emsp;Surat Ka Kanwil Depdikbud Prop. 
<br>&emsp;Jateng tgl. 12-5-1997 No. 01764/I03.a/F/1997 SMP Negeri 06 Semarang tersebut bahwa <br>&emsp;gedung sekolah sudah 
berusia + 63 tahun berjalan.<br><br>
<h2>Letak Geografis</h2><br><br>
&emsp;&emsp;SMP Negeri 6 yang beralamat di Jalan Pattimura No. 9 Semarang, secara geografis<br>&emsp; terletak di Kelurahan 
Kebon Agung, Kecamatan Semarang Timur, Kota Semarang,<br>&emsp; mudah dijangkau dari berbagai arah melalui banyak 
alat transpotasi. Sebagai sekolah<br>&emsp; kategori SSN Mandiri semenjak tahun 2008, SMP Negeri 6 Semarang 
merupakan salah<br>&emsp; satu sekolah yang menjadi favorit masyarakat Kota Semarang bagian timur dan utara.  
<br>&emsp;Hal<br>&emsp; itu karena kualitas akademik maupun non akademik yang dipandang baik oleh<br>&emsp; masyarakat.<br>&emsp; Kualitas 
akademik relatif tinggi ditinjau dari perolehan rata-rata nilai hasil Ujian<br>&emsp; Nasional<br>&emsp; yang dari tahun ke 
tahun selalu meningkat Berdasarkan hasil analisis konteks dapat<br>&emsp; dikemukakan kekuatan, kelemahan, peluang 
dan tantangan yang dihadapi sekolah.
